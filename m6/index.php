                <p>
                    <b>Mohamed Salah Hamed Mahrous Ghaly</b> (<a href="https://en.wikipedia.org/wiki/Arabic_language" class="mw-redirect" title="Arabic language">Arabic</a>: <span lang="ar" dir="rtl">محمد صلاح حامد محروس غالي</span>‎, <small>Egyptian Arabic pronunciation:&nbsp;</small><span title="Representation in the International Phonetic Alphabet (IPA)" class="IPA"><a href="https://en.wikipedia.org/wiki/Help:IPA/Egyptian_Arabic" title="Help:IPA/Egyptian Arabic">[mæˈħam.mæd sˤɑˈlɑːħ ˈɣæːli]</a></span>; <i>born 15 June 1992 </i>) is an Egyptian professional <a href="https://en.wikipedia.org/wiki/Association_football" title="Association football">footballer</a> who plays as a <a href="https://en.wikipedia.org/wiki/Forward_(association_football)" title="Forward (association football)">forward</a> for <a href="https://en.wikipedia.org/wiki/Premier_League" title="Premier League">Premier League</a> club <a href="https://en.wikipedia.org/wiki/Liverpool_F.C." title="Liverpool F.C.">Liverpool</a> and <a href="https://en.wikipedia.org/wiki/Captain_(association_football)" title="Captain (association football)">captains</a> the <a href="https://en.wikipedia.org/wiki/Egypt_national_football_team" title="Egypt national football team">Egypt national team</a>. Considered one of the best players in the world, he is known for his finishing, dribbling, and speed.
                </p>
                <p>
                    Salah started his senior career with Egyptian club <a href="https://en.wikipedia.org/wiki/Al_Mokawloon_Al_Arab_SC" title="Al Mokawloon Al Arab SC">Al Mokawloon</a>, departing shortly thereafter to join Swiss side <a href="https://en.wikipedia.org/wiki/Al_Mokawloon_Al_Arab_SC" title="FC Basel">Basel</a> for an undisclosed fee. In Switzerland, his performances attracted Premier League side <a href="https://en.wikipedia.org/wiki/Chelsea_F.C." title="Chelsea F.C.">Chelsea</a>, who signed him in 2014 for a reported fee of £11&nbsp;million. However, he was used sparingly in his debut season and was allowed to leave on loan to <a href="https://en.wikipedia.org/wiki/Serie_A" title="Serie A">Serie A</a> clubs <a href="https://en.wikipedia.org/wiki/ACF_Fiorentina" title="ACF Fiorentina">Fiorentina</a> and <a href="https://en.wikipedia.org/wiki/A.S._Roma" title="A.S. Roma">Roma</a>, with the latter eventually signing him permanently for €15&nbsp;million. Following his role in helping Roma to a <a href="https://en.wikipedia.org/wiki/2016%E2%80%9317_A.S._Roma_season" title="2016–17 A.S. Roma season">second-placed finish and a club record points tally</a> in 2017, Salah returned to the Premier League to sign for Liverpool for a then club-record fee of £36.9&nbsp;million.
                </p>
                <p>
                    In his second spell in England, Salah adapted his game from a natural winger to a forward, and he netted 32 goals in 36 league games in his debut season, winning the <a href="https://en.wikipedia.org/wiki/Premier_League_Golden_Boot" title="Premier League Golden Boot">Premier League Golden Boot</a> and breaking the Premier League scoring record for a 38-game season. Salah's performances earned him a number of individual accolades, including the <a href="https://en.wikipedia.org/wiki/PFA_Players%27_Player_of_the_Year" title="PFA Players' Player of the Year">PFA Players' Player of the Year</a>. He also came third in the <a href="https://en.wikipedia.org/wiki/The_Best_FIFA_Men%27s_Player#Winners" title="The Best FIFA Men's Player">2018 Best FIFA Men's Player</a> award. The <a href="https://en.wikipedia.org/wiki/2018%E2%80%9319_Liverpool_F.C._season" title="2018–19 Liverpool F.C. season">following season</a>, he finished as the Premier League's joint-top goalscorer and helped Liverpool win the <a href="https://en.wikipedia.org/wiki/2018%E2%80%9319_UEFA_Champions_League" title="2018–19 UEFA Champions League">2018–19 UEFA Champions League</a>, scoring in the final. In his third season at the club, he helped the team win the <a href="" title="2019–20 Premier League">2019–20 Premier League</a>, which ended the club's 30-year league title drought.
                </p>
                <p>
                    At international level, Salah represented Egypt at youth level before making his senior debut in 2011. Following his performances at the <a href="https://en.wikipedia.org/wiki/2012_Summer_Olympics" title="2012 Summer Olympics">2012 Summer Olympics</a>, he was named <a href="https://en.wikipedia.org/wiki/CAF_Awards" title="CAF Awards">CAF Most Promising African Talent</a> of the Year. Since then, he has helped Egypt reach the final of the <a href="https://en.wikipedia.org/wiki/2017_Africa_Cup_of_Nations" title="2017 Africa Cup of Nations">2017 Africa Cup of Nations</a>, and was top scorer during <a href="https://en.wikipedia.org/wiki/2018_FIFA_World_Cup_qualification_(CAF)" title="2018 FIFA World Cup qualification (CAF)">CAF qualification</a> to help the team qualify for the <a href="https://en.wikipedia.org/wiki/2018_FIFA_World_Cup" title="2018 FIFA World Cup">2018 FIFA World Cup</a>. Salah has been named the <a href="https://en.wikipedia.org/wiki/African_Footballer_of_the_Year" title="African Footballer of the Year">CAF African Footballer of the Year</a> (2017 and 2018), <a href="https://en.wikipedia.org/wiki/BBC_African_Footballer_of_the_Year" title="BBC African Footballer of the Year">BBC African Footballer of the Year</a> (2017 and 2018), and was selected in the <a href="https://en.wikipedia.org/wiki/BBC_African_Footballer_of_the_Year" title="2017 Africa Cup of Nations">2017 Africa Cup of Nations Team of the Tournament</a> and the <a href="https://en.wikipedia.org/wiki/CAF_Team_of_the_Year" title="CAF Team of the Year">CAF Team of the Year</a> on several occasions.
                </p>
